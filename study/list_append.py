# 답변:
# 문제는 파이썬에서 리스트는 변경 가능한(mutable) 데이터 구조이기 때문에 발생합니다.
# 리스트를 다른 리스트에 직접 추가하면 실제로는 동일한 리스트 객체에 대한 참조가 추가됩니다.
# 즉, 같은 리스트 객체를 참조하게 되는 것이죠. 그러면 해당 리스트의 변경이 결과에도 영향을 미치게 됩니다.
#
# 예를 들어, 다음과 같은 경우를 고려해 봅시다:


results = []
selected_nums = [1, 2, 3]

# selected_nums를 results에 추가
results.append(selected_nums)

# selected_nums를 변경
selected_nums.append(4)

print(results)  # 출력: [[1, 2, 3, 4]]


# 이 경우, selected_nums를 results 리스트에 추가한 후에 selected_nums를 변경하였지만,
# 결과는 [1, 2, 3, 4]가 아니라 [[1, 2, 3, 4]]입니다.
# 이는 results 리스트가 selected_nums 리스트를 직접 참조하고 있기 때문에 발생하는 문제입니다.
# 결과 리스트에 추가된 리스트와 원본 리스트는 동일한 리스트 객체를 참조하고 있으므로, 한 쪽에서 변경이 일어나면 다른 쪽에도 영향을 미치게 됩니다.
#
# 따라서 원본 리스트의 변경이 결과에도 영향을 미치지 않기 위해서는, 결과 리스트에 추가할 때 원본 리스트의 복사본을 추가해야 합니다.
# 이렇게 하면 결과 리스트는 별도의 리스트 객체를 참조하게 되므로, 원본 리스트의 변경과는 상관없이 올바른 결과를 얻을 수 있습니다.